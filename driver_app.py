def calculate_transaction(app, price, tip, pay_method, settings):
    # 1. ‡∏´‡∏≤ % GP ‡∏Ç‡∏≠‡∏á‡πÅ‡∏≠‡∏õ‡∏ô‡∏±‡πâ‡∏ô
    gp_rate = 0.0
    if app == "Maxim": gp_rate = settings.get("maxim_rate", 15) / 100
    elif app == "Grab": gp_rate = settings.get("grab_rate", 0) / 100
    elif app == "Bolt": gp_rate = settings.get("bolt_rate", 0) / 100
    elif app == "Win/‡∏á‡∏≤‡∏ô‡∏ô‡∏≠‡∏Å": gp_rate = 0.0 # ‡∏á‡∏≤‡∏ô‡∏ô‡∏≠‡∏Å‡πÑ‡∏°‡πà‡∏´‡∏±‡∏Å
    
    # 2. ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏Ñ‡πà‡∏≤‡∏Ñ‡∏≠‡∏° (Deduction)
    deduction = price * gp_rate
    
    # 3. ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏Å‡∏≥‡πÑ‡∏£‡∏™‡∏∏‡∏ó‡∏ò‡∏¥ (Net Income) - ‡∏ó‡∏≤‡∏á‡∏ö‡∏±‡∏ç‡∏ä‡∏µ
    net_income = price - deduction + tip
    
    # 4. ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏Å‡∏£‡∏∞‡πÅ‡∏™‡πÄ‡∏á‡∏¥‡∏ô‡∏™‡∏î (Cash vs Wallet) - ‡∏™‡∏≥‡∏Ñ‡∏±‡∏ç‡∏°‡∏≤‡∏Å!
    cash_pocket = 0.0
    wallet_impact = 0.0
    
    if pay_method == "üíµ ‡πÄ‡∏á‡∏¥‡∏ô‡∏™‡∏î/‡πÇ‡∏≠‡∏ô‡πÄ‡∏Ç‡πâ‡∏≤‡πÄ‡∏£‡∏≤":
        # ‡∏£‡∏±‡∏ö‡πÄ‡∏á‡∏¥‡∏ô‡∏™‡∏î: ‡πÄ‡∏£‡∏≤‡∏ñ‡∏∑‡∏≠‡πÄ‡∏á‡∏¥‡∏ô‡πÄ‡∏ï‡πá‡∏°(‡∏£‡∏ß‡∏°‡∏ó‡∏¥‡∏õ) ‡πÅ‡∏ï‡πà‡πÅ‡∏≠‡∏õ‡∏à‡∏∞‡∏´‡∏±‡∏Å‡∏Ñ‡πà‡∏≤‡∏Ñ‡∏≠‡∏°‡∏≠‡∏≠‡∏Å‡∏à‡∏≤‡∏Å‡∏Å‡∏£‡∏∞‡πÄ‡∏õ‡πã‡∏≤‡πÄ‡∏Ñ‡∏£‡∏î‡∏¥‡∏ï
        cash_pocket = price + tip
        wallet_impact = -deduction 
    else: # "üí≥ ‡∏ï‡∏±‡∏î‡∏ö‡∏±‡∏ï‡∏£/Wallet‡πÅ‡∏≠‡∏õ"
        # ‡∏£‡∏±‡∏ö‡∏ú‡πà‡∏≤‡∏ô‡πÅ‡∏≠‡∏õ: ‡πÄ‡∏£‡∏≤‡πÑ‡∏°‡πà‡πÑ‡∏î‡πâ‡∏à‡∏±‡∏ö‡πÄ‡∏á‡∏¥‡∏ô ‡πÅ‡∏≠‡∏õ‡πÄ‡∏Å‡πá‡∏ö‡πÑ‡∏õ ‡πÅ‡∏•‡πâ‡∏ß‡πÄ‡∏≠‡∏≤‡∏™‡πà‡∏ß‡∏ô‡∏ó‡∏µ‡πà‡πÄ‡∏´‡∏•‡∏∑‡∏≠(‡∏´‡∏•‡∏±‡∏á‡∏´‡∏±‡∏Å)‡πÑ‡∏õ‡πÄ‡∏ï‡∏¥‡∏°‡πÉ‡∏™‡πà‡∏Å‡∏£‡∏∞‡πÄ‡∏õ‡πã‡∏≤‡πÄ‡∏Ñ‡∏£‡∏î‡∏¥‡∏ï‡πÉ‡∏´‡πâ‡πÄ‡∏£‡∏≤
        cash_pocket = 0.0 # ‡πÑ‡∏°‡πà‡πÑ‡∏î‡πâ‡πÄ‡∏á‡∏¥‡∏ô‡∏™‡∏î‡πÄ‡∏•‡∏¢
        wallet_impact = (price - deduction) + tip # ‡πÄ‡∏á‡∏¥‡∏ô‡πÄ‡∏Ç‡πâ‡∏≤ Wallet (‡∏£‡∏ß‡∏°‡∏ó‡∏¥‡∏õ‡∏ñ‡πâ‡∏≤‡∏°‡∏µ)

    return deduction, net_income, cash_pocket, wallet_impact

